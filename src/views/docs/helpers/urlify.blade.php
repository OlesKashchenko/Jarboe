
<p>
    Используется для преобразования строки в корректную для использования в URL.<br>
</p>

<pre>
<code class="php">
echo Jarboe::urlify(" J'étudie le français ");
// jetudie-le-francais

echo Jarboe::urlify('Lo siento, no hablo español.');
// lo-siento-no-hablo-espanol
</code>
</pre>            

